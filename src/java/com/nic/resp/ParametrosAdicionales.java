public class ParametrosAdicionales {

    private String lineaAdicional;
    private String tipoRegistroAdicional;
    private String talorAdicional;

    public String getLineaAdicional() {
        return lineaAdicional;
    }

    public void setLineaAdicional(String lineaAdicional) {
        this.lineaAdicional = lineaAdicional;
    }

    public String getTipoRegistroAdicional() {
        return tipoRegistroAdicional;
    }

    public void setTipoRegistroAdicional(String tipoRegistroAdicional) {
        this.tipoRegistroAdicional = tipoRegistroAdicional;
    }

    public String getTalorAdicional() {
        return talorAdicional;
    }

    public void setTalorAdicional(String talorAdicional) {
        this.talorAdicional = talorAdicional;
    }

    
}
